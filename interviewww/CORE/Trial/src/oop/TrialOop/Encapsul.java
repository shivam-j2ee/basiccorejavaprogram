package oop.TrialOop;

public class Encapsul {

	private String name;
	private String address;
	
	public String getname() {
		return name;
	}
	public void setname(String name) {
		this.name=name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
}

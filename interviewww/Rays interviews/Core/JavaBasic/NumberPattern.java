package com.raystech.JavaBasic;

public class NumberPattern {

	public static void main(String[] args) {

		for(int i=1;i<=5;i++){
			for(int j=1;j<=i;j++){
				System.out.print(i);
			}
			System.out.println("");
		}for(int k=4;k>=1;k--){
			for(int j=1;j<=k;j++){
				System.out.print(k);
			}System.out.println("");
		}
	}

}
